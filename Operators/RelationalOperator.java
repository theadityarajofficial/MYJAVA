public class RelationalOperator {
    public static void main(String[] args) {
        int A=5;
        int B=10;
        System.out.println(A==B);//false
        int C=5;
        System.out.println(A==C);//true
        System.out.println(A!=B);//true
        System.out.println(A!=C);//false
        System.out.println(A>B);//false
        System.out.println(A<B);//true
        System.out.println(A>=C);//true
        System.out.println(A<=B);//true)
    }
}
